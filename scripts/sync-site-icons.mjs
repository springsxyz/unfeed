/**
 * Sync popup site marks from simple-icons (monochrome, currentColor).
 * Run: npm run sync:icons
 *
 * LinkedIn was removed from simple-icons (trademark). Path kept as a local fallback
 * from the last public Simple Icons release that included it (CC0-1.0).
 */
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";

const require = createRequire(import.meta.url);
const siPkg = dirname(require.resolve("simple-icons"));
const iconsDir = join(siPkg, "icons");

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const outPath = join(root, "popup", "site-icons.js");

/** @type {Record<string, string>} */
const FALLBACK_PATHS = {
  // Last Simple Icons LinkedIn mark before removal (CC0-1.0)
  linkedin:
    "M20.447 20.452h-3.554v-5.569c0-1.328-.027-3.037-1.852-3.037-1.853 0-2.136 1.445-2.136 2.939v5.667H9.351V9h3.414v1.561h.046c.477-.9 1.637-1.85 3.37-1.85 3.601 0 4.267 2.37 4.267 5.455v6.286zM5.337 7.433c-1.144 0-2.063-.926-2.063-2.065 0-1.138.92-2.063 2.063-2.063 1.14 0 2.064.925 2.064 2.063 0 1.139-.925 2.065-2.064 2.065zm1.782 13.019H3.555V9h3.564v11.452zM22.225 0H1.771C.792 0 0 .774 0 1.729v20.542C0 23.227.792 24 1.771 24h20.451C23.2 24 24 23.227 24 22.271V1.729C24 .774 23.2 0 22.222 0h.003z",
};

const ICONS = [
  ["blueskyEnabled", "bluesky"],
  ["facebookEnabled", "facebook"],
  ["instagramEnabled", "instagram"],
  ["linkedinEnabled", "linkedin"],
  ["pinterestEnabled", "pinterest"],
  ["redditEnabled", "reddit"],
  ["substackEnabled", "substack"],
  ["threadsEnabled", "threads"],
  ["tiktokEnabled", "tiktok"],
  ["xEnabled", "x"],
  ["youtubeEnabled", "youtube"],
];

function pathForSlug(slug) {
  const file = join(iconsDir, `${slug}.svg`);
  if (existsSync(file)) {
    const svg = readFileSync(file, "utf8");
    const match = svg.match(/<path\s[^>]*\bd="([^"]+)"/);
    if (!match) throw new Error(`No path in ${file}`);
    return match[1];
  }
  if (FALLBACK_PATHS[slug]) return FALLBACK_PATHS[slug];
  throw new Error(`Missing simple-icons mark: ${slug}`);
}

function svgMarkup(d) {
  return `<svg class="site-icon" viewBox="0 0 24 24" aria-hidden="true"><path fill="currentColor" d="${d}"/></svg>`;
}

const entries = ICONS.map(([id, slug]) => {
  const d = pathForSlug(slug);
  return `  ${id}: ${JSON.stringify(svgMarkup(d))},`;
}).join("\n");

const body = `/* Generated by scripts/sync-site-icons.mjs — do not edit by hand.
 * Source: simple-icons (CC0-1.0). Re-run: npm run sync:icons
 */
const SITE_ICONS = {
${entries}
};
`;

writeFileSync(outPath, body, "utf8");
console.log(`Wrote ${outPath} (${ICONS.length} icons from ${iconsDir})`);
